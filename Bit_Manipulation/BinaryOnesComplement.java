package Bit_Manipulation;

public class BinaryOnesComplement {
    public static void main(String[] args) {
        System.out.println(~5);
    }
    
    // Binary Ones Complement is also called NOT Operator
    // in this we have to calculate 2's complement
    // least significant bit (LSB) => most right digit
    // most significant bit (MSB) => most left digit 
    //if MSB is 0 => number is +ve
    // if MSB is 1 => number is -ve
}
